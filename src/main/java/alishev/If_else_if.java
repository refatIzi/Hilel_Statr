package alishev;

public class If_else_if {
    public static void main(String[] args) {
        System.out.println("Поднимаем Гирю");
        for(int i=0;i<=30;i=i+5) {
            if(i==0) System.out.println("Start");
            else if(i==5) System.out.println("Хорошое начало");
            else if(i==10) System.out.println("отлично");
            else if(i==15) System.out.println("не плохо");
            else if(i==20) System.out.println("думаю хватит");
            else if(i==25) System.out.println("пребор");
            else System.out.println("Походу тьі надорвался");


        }
    }

}
